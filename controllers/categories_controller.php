<?php
class CategoriesController extends AppController {

	var $name = 'Categories';

	
	function admin_index() {
		$this->Category->recursive = 0;
		$ids = $this->Category->find('list', array('fields'=>'id', 'parent_id'=>0));
		foreach($ids as $id){
			$this->Category->reorder(array(
				'id' => $id,    //id of record to use as top node for reordering, default: $Model->id
				'field' => 'name', //which field to use in reordering, default: $Model->displayField
				'order' => 'ASC', //direction to order, default: 'ASC'
				'verify' => true //whether or not to verify the tree before reorder, default: true
			));
		}
			
		$this->Category->recover();
	
		$this->set('categories', $this->paginate());
	}

	function admin_view($id = null) {
		if (!$id) {
			$this->Session->setFlash(__('Invalid category', true));
			$this->redirect(array('action' => 'index'));
		}
		$this->set('category', $this->Category->read(null, $id));
	}

	function admin_add() {
		if (!empty($this->data)) {
			$this->Category->create();
			if ($this->Category->save($this->data)) {
				$this->Session->setFlash(__('The category has been saved', true));
				$this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The category could not be saved. Please, try again.', true));
			}
		}
		$parents[0] = "[ No Parent ]";
		$Categorylist = $this->Category->generatetreelist(null,null,null," - ");
		if($Categorylist){
			foreach ($Categorylist as $key=>$value){
				$parents[$key] = $value;
			}
		}
		$this->set(compact('parents'));
	}

	function admin_edit($id = null) {
		if (!$id && empty($this->data)) {
			$this->Session->setFlash(__('Invalid category', true));
			$this->redirect(array('action' => 'index'));
		}
		if (!empty($this->data)) {
			if ($this->Category->save($this->data)) {
				$this->Session->setFlash(__('The category has been saved', true));
				$this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The category could not be saved. Please, try again.', true));
			}
		}
		if (empty($this->data)) {
			$this->data = $this->Category->read(null, $id);
		}
	}

	function admin_delete($id = null) {
		if (!$id) {
			$this->Session->setFlash(__('Invalid id for category', true));
			$this->redirect(array('action'=>'index'));
		}
		if ($this->Category->delete($id)) {
			$this->Session->setFlash(__('Category deleted', true));
			$this->redirect(array('action'=>'index'));
		}
		$this->Session->setFlash(__('Category was not deleted', true));
		$this->redirect(array('action' => 'index'));
	}
	
	function client_index() {
		$this->Category->recursive = 0;
		$this->set('categories', $this->paginate());
	}

